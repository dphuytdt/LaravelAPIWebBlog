    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $category = Category::all();
        $post = Post::with('category')->where('post_category_id', $id)->get();
        //find post_recommend  where not same post_id
        $post_recommend = Post::where('post_id', '!=', $id)->inRandomOrder()->take(3)->get();
        $title_category = Category::find($id);
        return view('pages.category-details', ['categories' => $category, 'posts' => $post, 'title_category' => $title_category, 'post_recommend' => $post_recommend]);
    }

