    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $category = Category::all();
        $post = Post::with('category')->where('post_id', $id)->first();
        //get all post form id except current post
        $post_newsest = Post::where('post_id', '!=', $id)->inRandomOrder()->take(3)->get();
        return view('pages.details', ['post' => $post], ['categories' => $category, 'post_newsest' => $post_newsest]);
    }

